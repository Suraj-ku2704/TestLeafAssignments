package week1.day2;

public class PractiseForForLoop {

	public static void main(String[] args) {
		
		//Print 1 to 10
		for (int i = 1; i <= 10; i++) {
			
			System.out.println(i);
			

		}
		System.out.println("Loop 1 is over");
		
		//Print 20 to 10
		for (int j = 20; j >= 10; j--) {
			
			System.out.println(j);
		}
		System.out.println("Loop 2 is over");
		

	}

}
